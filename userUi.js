
if (getScreenWidth() <= 560) {
    //For small screens
    mmForHelpTop = 170;
    mmForAboutTop = 220;

} else if (getScreenWidth() > 560 && getScreenWidth() <= 820) {
    //For medium-sized screens

    mmForHelpTop = 230;
    mmForAboutTop = 260;

} else {
    //For large screens
    mmForHelpTop = 250;
    mmForAboutTop = 280;

}
